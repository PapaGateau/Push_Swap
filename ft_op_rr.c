#include "push_swap.h"

void		ft_op_rr(t_stack **a, t_stack **b, int usage)
{
	ft_op_ra(a, b, 0);
	ft_op_rb(a, b, 0);
	if (usage)
		write(1, "rr\n", 3);
	if (usage == 2)
		print_stacks(a, b);
}
